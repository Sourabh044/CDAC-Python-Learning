# int to str
a = 8351
print('Type Before',a,type(a))
print('Type After of',a,type(str(a)))
# Float to string
b = 12.5
print('Type Before',b,type(b))
print('Type After of',b,type(str(b)))
# str to int
c = '4'
print('Type Before',c,type(c))
print('Type After of',c,type(int(c)))
# String to float
d = '12.5'
print('Type Before',d,type(d))
print('Type After of',d,type(float(d)))